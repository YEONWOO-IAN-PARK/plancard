
INSERT INTO CARD_SCRAPS (CARD_ID, USER_ID)
(
	SELECT 	ID, 2
	FROM	CARDS
	WHERE	MOD(CAST(ID AS UNSIGNED), 2) = 0
		AND IS_ACTIVE = TRUE
)
;

COMMIT;


INSERT INTO CARD_SCRAPS (CARD_ID, USER_ID)
(
	SELECT 	ID, 1
	FROM	CARDS
	WHERE	MOD(CAST(ID AS UNSIGNED), 2) != 0
		AND IS_ACTIVE = TRUE
)
;

COMMIT;


